const addEvolvesFromData = (evolvesFrom) => {
  const evolvesFromHolder = document.getElementById("evolves-from");
  const evolvesFromLink = document.createElement("a");
  evolvesFromLink.href = "#";
  evolvesFromLink.textContent =
    evolvesFrom[0].toUpperCase() + evolvesFrom.slice(1);
  evolvesFromHolder.textContent = "Evolves from \u2192 ";
  evolvesFromHolder.appendChild(evolvesFromLink);
  evolvesFromLink.addEventListener("click", async (e) => {
    e.preventDefault();
    const data = await getPokemonData(evolvesFrom);
    displayPokemonData(data);
  });
};

