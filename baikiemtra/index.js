const getPokemonData = async (pokemon = "greninja") => {
  const pokeApi = `https://pokeapi.co/api/v2/pokemon/${pokemon.toLowerCase()}`;
  try {
    const response = await fetch(pokeApi);
    const data = await response.json();
    const {
      weight,
      height,
      name,
      types,
      sprites: { front_default: image },
      species: { url: speciesUrl }
    } = data;

    const speciesResponse = await fetch(speciesUrl);
    const speciesData = await speciesResponse.json();
    const { evolves_from_species, habitat: habitatObj } = speciesData;
    const evolvesFrom = evolves_from_species?.name || null;
    const habitat =
      habitatObj?.name[0].toUpperCase() + habitatObj?.name.slice(1) ||
      "Unspecified";

    const typesList = types.map(
      ({ type: { name } }) => name[0].toUpperCase() + name.slice(1)
    );
    return {
      found: true,
      name,
      weight,
      height,
      typesList,
      image,
      evolvesFrom,
      habitat
    };
  } catch (e) {
    console.error(e.message);
    return { found: false, message: e.message };
  }
};

